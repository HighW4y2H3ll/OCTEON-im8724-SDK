int early_board_init(void)
{
	octeon_board_get_descriptor(CVMX_BOARD_TYPE_EAP7000_REF,
				    CONFIG_OCTEON_EAP7000_REF_MAJOR,
				    CONFIG_OCTEON_EAP7000_REF_MINOR);

	octeon_board_get_clock_info(OCTEON_EAP7000_REF_DEF_DRAM_FREQ);

	/* CN63XX has a fixed 50 MHz reference clock */
	gd->arch.ddr_ref_hertz = 50000000;

	octeon_board_get_mac_addr();

	return 0;
}

void late_board_init(void)
{
	char buf[256];
	char *board_name;
	board_name = getenv("boardname");
	if (board_name) {
		sprintf(buf, "u-boot-octeon_%s.bin", board_name);
		setenv("octeon_stage3_bootloader", buf);
	}
}
