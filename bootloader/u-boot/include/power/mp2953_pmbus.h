/* I2C device address for pmic mp2953 (0x21 default) */
#define MP2953_I2C_ADDR (0x42 >> 1)

enum {
	REG_DISABLE = 0,
	REG_ENABLE
};

enum {
	LDO_OFF = 0,
	LDO_ON,

	DIS_LDO = (0x00 << 6),
	EN_LDO = (0x3 << 6),
};

/* Buck1 1 volt value */
#define MP2953_BUCK1OUT_1V	0x5
/* Buck1 1.05 volt value */
#define MP2953_BUCK1OUT_1_05V    0x6
#define MP2953_BUCK1CTRL_EN	(3 << 0)
/* Buck2 1.3 volt value */
#define MP2953_BUCK2DVS1_1_3V	0x38
#define MP2953_BUCK2CTRL_ON	(1 << 4)
/* Buck3 1.0125 volt value */
#define MP2953_BUCK3DVS1_1_0125V	0x21
#define MP2953_BUCK3CTRL_ON	(1 << 4)
/* Buck4 1.2 volt value */
#define MP2953_BUCK4DVS1_1_2V	0x30
#define MP2953_BUCK4CTRL_ON	(1 << 4)
/* LDO2 1.5 volt value */
#define MP2953_LD02CTRL1_1_5V	0x1c
/* LDO3 1.8 volt value */
#define MP2953_LD03CTRL1_1_8V	0x14
/* LDO5 1.8 volt value */
#define MP2953_LD05CTRL1_1_8V	0x14
/* LDO10 1.8 volt value */
#define MP2953_LD10CTRL1_1_8V	0x14
/*
 * MP2953_REG_PMIC_32KHZ set to 32KH CP
 * output is activated
 */
#define MP2953_32KHCP_EN	(1 << 1)
/*
 * MP2953_REG_PMIC_BBAT set to
 * Back up batery charger on and
 * limit voltage setting to 3.5v
 */
#define MP2953_BBCHOSTEN	(1 << 0)
#define MP2953_BBCVS_3_5V	(3 << 3)
